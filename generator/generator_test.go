package generator

import (
	"fmt"
	"testing"

	// test named imports.
	req "github.com/stretchr/testify/require"
)

const gofile = "generator_test.go"
const optionsStruct = "TestOptions"

func TestGetImports(t *testing.T) {
	imports, err := GetFileImports(gofile)
	req.NoError(t, err)
	requiredImports := []string{
		`"fmt"`,
		`"testing"`,
		`req "github.com/stretchr/testify/require"`,
	}
	req.Equal(t, requiredImports, imports)
}

func TestGetOptionSpec(t *testing.T) {
	data, err := GetOptionSpec(gofile, optionsStruct)
	req.NoError(t, err)
	req.Equal(t, []OptionMeta{
		{
			Name:  "Stringer",
			Field: "stringer",
			Type:  "fmt.Stringer",
			TagOption: TagOption{
				IsRequired: true,
				IsNotEmpty: true,
			},
		},
		{
			Name:  "Str",
			Field: "str",
			Type:  "string",
			TagOption: TagOption{
				IsRequired: false,
				IsNotEmpty: true,
			},
		},
		{
			Name:  "SomeMap",
			Field: "someMap",
			Type:  "map[string]string",
			TagOption: TagOption{
				IsRequired: true,
				IsNotEmpty: true,
			},
		},
		{
			Name:  "NoValidation",
			Field: "noValidation",
			Type:  "string",
			TagOption: TagOption{
				IsRequired: false,
				IsNotEmpty: false,
			},
		},
	}, data)
}

func TestRenderOptions(t *testing.T) {
	data, err := GetOptionSpec(gofile, optionsStruct)
	req.NoError(t, err)

	imports, err := GetFileImports(gofile)
	req.NoError(t, err)

	res, err := RenderOptions("generator", optionsStruct, imports, data)
	req.NoError(t, err)

	req.Equal(t, testStructGenerated, string(res), "generated file:\n%v", string(res))
}

func TestToStopCIFromComplaining(t *testing.T) {
	s := TestOptions{
		stringer:     nil,
		str:          "123",
		noValidation: "qwe",
	}
	req.Equal(t, TestOptions{
		stringer:     nil,
		str:          "123",
		noValidation: "qwe",
	}, s)
}

